      <h1>Qui som?</h1>
      <p><strong>Pi Cloud</strong> és un projecte pensat per facilitar la pujada i sincronització de fitxers amb serveis al núvol com <strong>Google Drive</strong> i <strong>Dropbox</strong>, a través d'una interfície web senzilla allotjada en una <strong>Raspberry Pi</strong>.</p>
      
      <p><strong>Què fem?</strong></p>
      <p>Dissenyem una plataforma local per a gestionar i transferir fitxers de manera segura entre l'ordinador de l'usuari i el seu espai al núvol. També permet sincronitzar els arxius des del núvol a la Raspberry automàticament mitjançant <strong>Rclone</strong>, convertint-la en una potent eina de còpies de seguretat.</p>
      <img src="img/pimodel3.png" alt="Raspberry Pi model 3">

      <p><strong>Per què ho fem?</strong></p>
      <p>Molts usuaris necessiten una manera senzilla i segura de centralitzar els seus arxius al núvol sense dependre de plataformes comercials de pagament. El nostre sistema permet controlar-ho tot des d’un dispositiu propi i petit com la Raspberry Pi.</p>
      
      <p><strong>Objectiu</strong></p>
      <p>Oferir una solució lleugera, modular i de fàcil implementació perquè qualsevol persona pugui crear el seu propi servei de pujada d’arxius personalitzat. Està pensat especialment per a usuaris que publiquen arxius sovint al núvol i volen una còpia automàtica al seu sistema Linux local.</p>
      <img src="img/3043597.png" alt="Transferència de fitxers segura">

      <p><strong>Finalitat</strong></p>
      <p>Democratitzar la gestió de fitxers al núvol, proporcionant una eina intuïtiva i oberta que pugui ser reutilitzada i adaptada a diferents entorns. L’usuari pot descarregar la solució completa i desplegar-la fàcilment al seu dispositiu.</p>
